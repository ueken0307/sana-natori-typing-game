var countChar = function(p){
  let tmp = {};
  for(let i of p){
    if(tmp[`${i.hiragana.length}`] == undefined){
      tmp[`${i.hiragana.length}`] = 1;
    }else{
      tmp[`${i.hiragana.length}`]++;
    }
  }
  return tmp;
}

